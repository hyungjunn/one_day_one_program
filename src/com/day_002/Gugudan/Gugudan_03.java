package com.day_002.Gugudan;

public class Gugudan_03 {
    public static void main(String[] args) {
        int i = 1;
        while (i < 10) {
            int result =  6 * i;
            System.out.println(result);
            i++;
        }

        for (int j = 1; j < 10; j++) {
            int result = 7 * j;
            System.out.println(result);
        }
    }
}
